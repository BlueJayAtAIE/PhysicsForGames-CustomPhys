#include "raylib.h"
#include "game.h"
#include "glm.hpp"

int main()
{
	// Initialization
	//--------------------------------------------------------------------------------------
	game gm;

	// Timestep is initialized to about 30 times/1 second.
	gm.init(800, 450, 1.0f / 30.0f);

	//bool shouldRun = true;
	//--------------------------------------------------------------------------------------

	// Main game loop
	while (gm.tick())
	{
		// Update
		//----------------------------------------------------------------------------------
		//shouldRun = gm.tick();

		// Physics updates.
		while (gm.shouldTickPhysics())
		{
			gm.tickPhysics();
		}

		//----------------------------------------------------------------------------------

		// Draw
		//----------------------------------------------------------------------------------
		gm.draw();
		//----------------------------------------------------------------------------------
	}

	// De-Initialization
	//--------------------------------------------------------------------------------------   
	gm.exit();
	//--------------------------------------------------------------------------------------

	return 0;
}